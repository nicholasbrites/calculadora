    public String retornaResposta()
    {
        for(int i = retorno.length() ; i > 0 ; i--)
        {
            if(retorno.substring(i-1, i).equals(" "))
            {
                intervalo = i;
                i = -1;
            }
        }
        corte = retorno.substring(intervalo);
        return corte;
    }
    
